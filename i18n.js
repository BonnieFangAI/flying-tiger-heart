// i18n Engine
let currentLang = localStorage.getItem('ftt-lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('ftt-lang', lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const t = translations[lang]?.[key];
        if (t) {
            el.innerHTML = t;
        }
    });

    // Update active lang button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Set font preference based on language
    if (lang === 'ja') {
        document.body.style.fontFamily = "'Inter', 'Noto Sans JP', sans-serif";
    } else if (lang === 'ko') {
        document.body.style.fontFamily = "'Inter', 'Noto Sans KR', sans-serif";
    } else if (lang === 'zh') {
        document.body.style.fontFamily = "'Inter', 'Noto Sans SC', sans-serif";
    } else {
        document.body.style.fontFamily = "'Inter', 'Noto Sans SC', 'Noto Sans JP', 'Noto Sans KR', sans-serif";
    }
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Lang button clicks
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
    });

    // Set initial language
    setLanguage(currentLang);
});
